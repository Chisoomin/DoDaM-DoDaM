package com.example.dodamver2;

public class PlayListItem {
    String mood;
    String music;
    String artist;
    String videoId;

    public PlayListItem() {

    }

    public PlayListItem(String mood, String music, String artist, String videoId) {
        this.mood = mood;
        this.music = music;
        this.artist = artist;
        this.videoId = videoId;
    }
}
